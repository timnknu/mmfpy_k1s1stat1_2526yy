# Визначення можливих розподілів людей по командах
#
# L - кількість людей в команді
# K - кількість команд
# L >= 1 + K
# N - кількість присутніх студентів
# 2 <= K <= N
# 1 + K <= L <= N
# і при цьому L*K <= N
N = 16

for K in range(2, N+1):
    for L in range(1+K, N+1):
        if L * K <= N:
            print(f'{K} команд по {L} учасників, що загалом: {K*L}')